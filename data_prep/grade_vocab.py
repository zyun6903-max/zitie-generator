def get_all_chars() -> list[dict]:
    """返回所有生字的列表 [{char, grade, lesson}, ...]"""
    result = []
    for grade, lessons in GRADE_VOCAB.items():
        for lesson_num, chars in lessons.items():
            for char in chars:
                result.append({"char": char, "grade": grade, "lesson": lesson_num})
    return result


def get_chars_by_grade(grade: int) -> list[dict]:
    """返回指定年级的生字列表。"""
    return [c for c in get_all_chars() if c["grade"] == grade]
